#include "libft.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	size_t	i;

	i = 0;
	while (s2[i])
	{
		s1[i] = s2[i];
		i++;
	}
	while (i <= ft_strlen(s1))
	{
		s1[i] = '\0';
		i++;
	}
	return (s1);
}
